CREATE DEFINER=`root`@`localhost` PROCEDURE `sp_punishReturnYet`()
BEGIN
    -- Xóa dữ liệu cũ trong bảng punishments
    DELETE FROM punishments;

    -- Chèn dữ liệu mới vào bảng punishments
    INSERT INTO punishments (loan_id, reader_id, fullname, book_id, book_name, loan_no, days_late, total, status)
    SELECT 
        l.loan_id,
        l.reader_id,
        r.name AS fullname,  -- Lấy tên độc giả từ bảng readers
        l.book_id,
        b.book_name AS book_name,  -- Lấy tên sách từ bảng books
        l.loan_no,  -- Số lượng sách mượn
        DATEDIFF(CURDATE(), l.book_return_appointment_date) AS days_late,  -- Tính số ngày quá hạn
        (DATEDIFF(CURDATE(), l.book_return_appointment_date) * 5000) AS total,  -- Tính tổng tiền phạt
        l.status  -- Trạng thái của khoản vay
    FROM 
        loans l
    JOIN 
        readers r ON l.reader_id = r.reader_id
    JOIN 
        books b ON l.book_id = b.book_id
    WHERE 
        l.book_return_appointment_date < CURDATE()
	AND l.status = 'active';
        
END ;;
